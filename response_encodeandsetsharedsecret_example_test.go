package spsp_test

import (
	"fmt"

	"github.com/reiver/go-spsp"
)

func ExampleResponse_EncodeAndSetSharedSecret() {

	var sharedSecret [32]byte = [32]byte{
		0x48,0x65,0x6C,0x6C,0x6F,0x20,0x77,0x6F,
		0x72,0x6C,0x64,0x21,0x0D,0x0A,0x48,0x6F,
		0x77,0x20,0x61,0x72,0x65,0x20,0x79,0x6F,
		0x75,0x20,0x74,0x6F,0x64,0x61,0x79,0x3F,
	}

	var response spsp.Response

	response.EncodeAndSetSharedSecret(sharedSecret)

	fmt.Printf("BASE64 ENCODED SHARED-SECRET: %s", response.SharedSecret)

	// Output:
	// BASE64 ENCODED SHARED-SECRET: SGVsbG8gd29ybGQhDQpIb3cgYXJlIHlvdSB0b2RheT8=
}
